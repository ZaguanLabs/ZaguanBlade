export interface FileIconResult {
    icon: React.ReactNode;
    color: string;
}

export function getFileIcon(filename: string, isActive: boolean = false): FileIconResult {
    const ext = filename.split('.').pop()?.toLowerCase() || '';
    const name = filename.toLowerCase();
    const baseClass = "w-3.5 h-3.5 shrink-0";
    
    // Special files
    if (name === '.gitignore' || name === '.gitattributes' || name === '.gitmodules') {
        return { icon: <GitIcon className={baseClass} />, color: isActive ? 'text-orange-400' : 'text-orange-500' };
    }
    
    // By extension
    switch (ext) {
        case 'rs':
            return { icon: <RustIcon className={baseClass} />, color: isActive ? 'text-orange-400' : 'text-orange-500' };
        case 'go':
            return { icon: <GoIcon className={baseClass} />, color: isActive ? 'text-cyan-400' : 'text-cyan-500' };
        case 'ts':
        case 'tsx':
            return { icon: <TypeScriptIcon className={baseClass} />, color: isActive ? 'text-blue-400' : 'text-blue-500' };
        case 'js':
        case 'jsx':
        case 'mjs':
        case 'cjs':
            return { icon: <JavaScriptIcon className={baseClass} />, color: isActive ? 'text-yellow-400' : 'text-yellow-500' };
        case 'md':
        case 'mdx':
        case 'markdown':
            return { icon: <MarkdownIcon className={baseClass} />, color: isActive ? 'text-blue-300' : 'text-blue-400' };
        case 'json':
        case 'jsonc':
            return { icon: <JsonIcon className={baseClass} />, color: isActive ? 'text-yellow-300' : 'text-yellow-400' };
        case 'html':
        case 'htm':
            return { icon: <HtmlIcon className={baseClass} />, color: isActive ? 'text-orange-400' : 'text-orange-500' };
        case 'css':
        case 'scss':
        case 'sass':
        case 'less':
            return { icon: <CssIcon className={baseClass} />, color: isActive ? 'text-blue-400' : 'text-blue-500' };
        case 'yaml':
        case 'yml':
            return { icon: <YamlIcon className={baseClass} />, color: isActive ? 'text-red-400' : 'text-red-500' };
        case 'toml':
            return { icon: <TomlIcon className={baseClass} />, color: isActive ? 'text-gray-300' : 'text-gray-400' };
        case 'txt':
        case 'text':
            return { icon: <DefaultFileIcon className={baseClass} />, color: isActive ? 'text-gray-300' : 'text-gray-400' };
        default:
            return { icon: <DefaultFileIcon className={baseClass} />, color: isActive ? 'text-zinc-400' : 'text-zinc-500' };
    }
}
